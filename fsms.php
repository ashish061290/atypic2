<p><strong>Food safety management systems</strong><br /> ISO 22000:2005 is a food safety management for organizations involved in food chain. This standard specifies the requirements for food safety management system where an organization in food chain needs to demonstrate its ability to control the food related hazards. This standard was prepared by Technical Committee ISO/TC 34, Food products. Food safety is related to the presence of food-borne hazards in food at the point of consumption. As the food safety hazards can occur at any stage of the food chain, adequate control throughout the food chain is essential. Thus, food safety is ensured through the combined efforts of all the parties participating in the food chain.</p>
<p><img class="size-medium wp-image-232 alignright" src="../www.piccert.com/wp-content/uploads/2016/12/download-s-300x166.html" alt="" width="300" height="166" /></p>
<p>This International Standard specifies the requirements for a food safety management system that combines the following generally recognized key elements to ensure food safety along the food chain, up to the point of final consumption:</p>
<p>* Interactive communication * System management * Prerequisite programmes * HACCP principles.</p>
<p>The main aim of this standard is to harmonize on a global level the requirements for food safety management for businesses within the food chain. It is particularly intended for application by organizations that seek a more focused, coherent and integrated food safety management system than is normally required by law. It requires an organization to meet any applicable food safety related statutory and regulatory requirements through its food safety management system.</p>
<p>This International Standard integrates the principles of the Hazard Analysis and Critical Control Point (HACCP) system and application steps developed by the Codex Alimentarius Commission. By means of auditable requirements, it combines the HACCP plan with prerequisite programmes (PRPs). Hazard analysis is the key to an effective food safety management system, since conducting a hazard analysis assists in organizing the knowledge required to establish an effective combination of control measures.</p>
