<p><strong>ISO 13485: 2003 Certification</strong> represents the requirements for a comprehensive management system for the design and manufacture of medical devices. All requirements of this International Standard are specific to organizations providing medical devices, regardless of the type or size of the organization. ISO 13485: 2003 Certification supersedes earlier documents such as EN 46001 and EN 46002 (both 1997), the ISO 13485 published in 1996 and ISO 13488 (1996). ISO 13485:2003 is a quality system standard designed specifically for medical device companies. The standard is applied by most Class II and III (plus Class IV in Canada) medical device manufacturers to meet the quality system requirements of Europe, Canada, Australia, Japan and other parts of the world.</p>
<p>Compliance with ISO 9001:2008 is often seen as the first step in achieving compliance with ISO 13485:2003, which is based on the ISO 9001:2008 process model, suggests that the application and management of a system of processes is an effective way to ensure good quality management. All requirements of this International Standard are specific to organizations providing Medical Devices, regardless of the type or size of the organization.</p>
<p>ISO 13485 in part designed to produce a management system that facilitates compliance to the requirements of customers and various global regulators. While being certified to ISO13485 does not fulfill the requirements of either the FDA or foreign regulators, the certification aligns an organization&rsquo;s management system to the requirements of the FDA&rsquo;s Quality System Regulation (QSR) requirements as well as many other regulatory requirements found throughout the world.</p>
<p>Therefore, ISO13485 certification serves to create a management system that can be thought of as a framework on which to build compliance to various regulatory and customer requirements. The ISO 13485 standard supplements ISO 9001 and has many of the same requirements. However, there are additional requirements for process control, design control, retention of records, accountability, traceability, customer satisfaction and more. Individual countries such as Canada and Japan have additional requirements necessary to meet their specific regulations. ISO 13485 certification is not accepted by the US FDA, although it shares many of the same characteristics as FDA Good Manufacturing Practice (GMP).</p>
